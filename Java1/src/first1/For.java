package first1;

public class For {

	public static void main(String[] args) {
		 int a = 1;
	        int b = 2;
	        int c;
	        int d;

	 

	        c = ++b;
	        System.out.println("print"+c);
	        d = a++;
	        System.out.println("print"+d);

	 

	        c++;
	        System.out.println("print"+c);

	 

	        System.out.println("a = " + a);
	        System.out.println("b = " + b);
	        System.out.println("c = " + c);
	        System.out.print("d = " + d);
	}

}
